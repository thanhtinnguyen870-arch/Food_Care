const ensureDefaultUsers = async () => {
  const defaults = [
    {
      name: process.env.SEED_ADMIN_NAME || "Admin User",
      email: process.env.SEED_ADMIN_EMAIL || "admin@foodcare.ai",
      password: process.env.SEED_ADMIN_PASSWORD,
      role: "admin",
    },
    {
      name: process.env.SEED_USER_NAME || "Test User",
      email: process.env.SEED_USER_EMAIL || "user@foodcare.ai",
      password: process.env.SEED_USER_PASSWORD,
      role: "user",
    },
  ].filter((item) => item.password);

  if (defaults.length === 0) {
    console.warn("Skipping default users: configure SEED_ADMIN_PASSWORD or SEED_USER_PASSWORD.");
  }

  for (const item of defaults) {
    const exists = await User.findOne({ email: item.email });
    if (!exists) {
      await User.create({
        name: item.name,
        email: item.email,
        password: await bcrypt.hash(item.password, 10),
        role: item.role,
      });
    }
  }
};

const importData = async () => {
  try {
    await ensureDefaultUsers();

    await Category.bulkWrite(
      categories.map((category) => ({
        updateOne: {
          filter: { slug: category.slug },
          update: { $set: category },
          upsert: true,
        },
      })),
    );

    const createdCategories = await Category.find({
      slug: { $in: categories.map((category) => category.slug) },
    });
    const catMap = Object.fromEntries(
      createdCategories.map((category) => [category.slug, category._id]),
    );

    const sampleFoods = foods.map(({ categorySlug, ...item }) => ({
      ...item,
      category: catMap[categorySlug],
    }));

    await Food.bulkWrite(
      sampleFoods.map((item) => ({
        updateOne: {
          filter: { slug: item.slug },
          update: { $set: item },
          upsert: true,
        },
      })),
    );

    console.log(
      `Data synchronized successfully: ${createdCategories.length} categories, ${sampleFoods.length} foods.`,
    );
    process.exit();
  } catch (error) {
    console.error(`Error: ${error.message}`);
    process.exit(1);
  }
};

importData();
