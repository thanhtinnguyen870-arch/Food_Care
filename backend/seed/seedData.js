const slugify = (value) =>
  value
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .trim()
    .replace(/đ/g, "d")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");

const kcal = (components) =>
  Math.round(
    components.reduce((total, item) => {
      const per100g = caloriePer100g[item.name];
      if (per100g === undefined) {
        throw new Error(`Missing calorie source for ingredient: ${item.name}`);
      }
      return total + (per100g * item.grams) / 100;
    }, 0),
  );

const ingredientCalories = (item) =>
  Math.round((caloriePer100g[item.name] * item.grams) / 100);

const ingredientText = (components) =>
  components.map(
    (item) => `${item.name} ${item.grams}g(${ingredientCalories(item)} calo)`,
  );

const estimateMacros = (calories, type) => {
  const ratios = {
    carb: [0.18, 0.58, 0.24],
    protein: [0.38, 0.38, 0.24],
    lowCarb: [0.42, 0.25, 0.33],
    drink: [0.08, 0.82, 0.1],
    balanced: [0.28, 0.48, 0.24],
  }[type || "balanced"];

  return {
    protein: Math.round((calories * ratios[0]) / 4),
    carbs: Math.round((calories * ratios[1]) / 4),
    fat: Math.round((calories * ratios[2]) / 9),
  };
};

